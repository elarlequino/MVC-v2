<h2>Notre Mission</h2>
<p>Depuis huit ans, l'Épise — Épicerie Solidaire des Étudiants — se tient aux côtés des jeunes qui en ont besoin. Parce que poursuivre ses études ne devrait jamais rimer avec renoncement, nous agissons chaque jour pour lutter contre la précarité étudiante à l’Université de la Nouvelle-Calédonie.</p>
<p>Pour une cotisation symbolique de 200 francs CFP par an, nos adhérent·e·s peuvent accéder à des denrées alimentaires, des produits d’hygiène, des vêtements, de la vaisselle, des livres... mais surtout à un peu de répit, à un lieu d’accueil chaleureux, à une main tendue quand le mois devient trop long.</p>
<p>Cette mission, nous ne la portons pas seul·e·s. Elle existe grâce à la générosité de la population calédonienne, à des entreprises solidaires, à des partenaires engagés, et bien sûr, grâce à notre formidable équipe de bénévoles.</p>
<p>À l'Épise, chaque don, chaque sourire, chaque panier distribué est un acte de résistance. Nous croyons en un monde étudiant, plus solidaire, plus humain.</p>
<p>Si vous ressentez le besoin d’être aidé·e, notre porte est ouverte. Si vous souhaitez aider, votre place est ici.</p>
